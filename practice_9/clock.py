import pygame
import math


def get_hand_angle(value, max_value):
    """
    Переводим значение времени в угол поворота для pygame.transform.rotate().
    
    pygame.transform.rotate() вращает ПРОТИВ часовой стрелки при положительных углах,
    но нам нужно по часовой. Поэтому угол берём отрицательным.
    
    Формула:
      - 0 секунд/минут = рука смотрит вверх (0°)
      - 15 сек/мин     = рука смотрит вправо (90°)
      - 30 сек/мин     = рука смотрит вниз (180°)
      - 45 сек/мин     = рука смотрит влево (270°)
    """
    # Вычисляем долю от полного оборота
    fraction = value / max_value
    # Переводим в градусы (360° = полный оборот), минус — по часовой стрелке
    angle = -fraction * 360
    return angle


def draw_hand(screen, original_image, angle, center_pos):
    """
    Рисует руку Микки Мауса на экране.
    
    Параметры:
      screen        — поверхность (окно)
      original_image— исходное изображение руки (не вращаем оригинал!)
      angle         — угол поворота в градусах
      center_pos    — (x, y) — центр циферблата, вокруг которого вращается рука
    
    ВАЖНО: pygame.transform.rotate() возвращает НОВУЮ поверхность,
    при этом её размер меняется. Поэтому используем get_rect(center=...)
    чтобы правильно вычислить позицию отрисовки.
    """
    # Получаем повёрнутое изображение (новая поверхность)
    rotated = pygame.transform.rotate(original_image, angle)
    
    # Вычисляем прямоугольник так, чтобы центр ротации совпал с центром циферблата
    rect = rotated.get_rect(center=center_pos)
    
    # Рисуем на экране
    screen.blit(rotated, rect)
